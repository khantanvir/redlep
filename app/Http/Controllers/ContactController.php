    public function usercontact(ContactRequest $request)
    {
        $contact = new Contact();
        $contact->name = $request->input('name');
        $contact->email = $request->input('email');
        $contact->phone = $request->input('phone');
        $contact->message = $request->input('message');
        $contact->save();
        $data['result'] = array(
            'key'=>200,
            'message' =>'Thanks for connecting with us. We will contact to you very soon:)'
        );
        return response()->json($data,200);
    }
    //list of public contact item 
    public function list(){
        $data['items'] = Contact::orderBy('id','desc')->paginate(10);
